            String sql = "INSERT INTO tmember VALUES ('"+tkode_member.getText()+"',"
                    + "'"+tnama_member.getText()+"','"+tno_member.getText()+"',"
                    + "'"+temail_member.getText()+"','"+tjk_member.getSelectedItem()+"',"
                    + "'"+tig_member.getText()+"','"+talamat_member.getText()+"')";
