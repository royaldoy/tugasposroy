    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.rowAtPoint(evt.getPoint());
        String member =jTable1.getValueAt(baris, 0).toString(); tkode_member.setText(member);
        String nama = jTable1.getValueAt(baris,1).toString();   tnama_member.setText(nama);
        String no = jTable1.getValueAt(baris, 2).toString();    tno_member.setText(no);
        String email=jTable1.getValueAt(baris, 3).toString();   temail_member.setText(email);
        String jk = jTable1.getValueAt(baris, 4).toString();    tjk_member.setSelectedItem(jk);
        String ig  = jTable1.getValueAt(baris, 5).toString();  tig_member.setText(ig);
        String alamat  = jTable1.getValueAt(baris, 6).toString();  talamat_member.setText(alamat);
        //txt_phone.setText(telp);        
    }//GEN-LAST:event_jTable1MouseClicked

